//Aula 13 - Operadores Relacionais


public class OperadoresRelacionais {
    
    public static void main (String[] args){
        
        int valor1 = 1;
        int valor2 = 2;
        
        System.out.println("Valor1 igual Valor2: " +(valor1 == valor2));
        System.out.println("Valor1 Diferente Valor2: " +(valor1!= valor2));
        System.out.println("Valor1 maior que Valor2: " +(valor1 > valor2));
        System.out.println("Valor1 menor Valor2: " +(valor1 < valor2));
        System.out.println("Valor1 maior ou igual Valor2: " +(valor1 >= valor2));
        System.out.println("Valor1 menor ou igual Valor2: " +(valor1 <= valor2));
    }
    
}
