public class LoopFor_3
{
    public static void main(String[] args)
    {
        //Variable
        //----------------------------------------------------------------------
        int soma = 0;          //Variable of Sum.
        //----------------------------------------------------------------------
        
        for (int i = 1; i < 5; soma += i++);
        System.out.println("Value of Sun: " +soma);
        
    }
}