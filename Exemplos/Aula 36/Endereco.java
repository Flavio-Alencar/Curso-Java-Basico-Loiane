package exemplos.Aula36;

public class Endereco {
    //                      DECLARAÇÃO DE VARIAVEIS GLOBAIS
    //--------------------------------------------------------------------------
    private  String endereco;
    private  String numero;
    private  String bairro;
    private  String cidade;
    private  String estado;
    private  String cep;
    //--------------------------------------------------------------------------

    public  String getEndereco() {
        return endereco;
    }

    public  void setEndereco(String aEndereco) {
        endereco = aEndereco;
    }

    public  String getNumero() {
        return numero;
    }

    public  void setNumero(String aNumero) {
        numero = aNumero;
    }

    public  String getBairro() {
        return bairro;
    }

    public  void setBairro(String aBairro) {
        bairro = aBairro;
    }

    public  String getCidade() {
        return cidade;
    }

    public  void setCidade(String aCidade) {
        cidade = aCidade;
    }

    public  String getEstado() {
        return estado;
    }

    public  void setEstado(String aEstado) {
        estado = aEstado;
    }

    public  String getCep() {
        return cep;
    }

    public  void setCep(String aCep) {
        cep = aCep;
    }
    
    
    
}
