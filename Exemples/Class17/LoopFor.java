public class LoopFor
{
    public static void main(String[] args)
    {
        for(int i = 0; i < 5; i = ++i)
        {
            System.out.println("Value of i: " +i);                        
        }
        
        for(int i = 5; i >= 0; i = --i)
        {
            System.out.println("Value of i: " +i);                        
        }
        
    }
}
