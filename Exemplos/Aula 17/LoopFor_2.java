public class LoopFor_2
{
    public static void main(String[] args)
    {
        //Variable
        //----------------------------------------------------------------------
        int count = 0;          //Variable Counter.
        //----------------------------------------------------------------------
        
        for (; count < 10;)
        {
            System.out.println("Value of Count: " +count);
            count +=2;
        }
    }
}